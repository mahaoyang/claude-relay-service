// Helper function to get Lucide icon name from Font Awesome class
export function getLucideIcon(faClass) {
  // Extract icon name from class string
  const match = faClass.match(/fa-([\w-]+)/)
  if (!match) return null

  const iconName = 'fa-' + match[1]
  return iconMapping[iconName] || null
}

// Helper function to convert all FA classes in a string
export function convertFAToLucide(str) {
  return str.replace(/class="(.*?)fa\s+(fa-[\w-]+)(.*?)"/g, (match, before, faIcon, after) => {
    const lucideIcon = getLucideIcon(faIcon)
    if (lucideIcon) {
      // Remove fas, far, fab classes and replace fa-* with lucide icon component
      const cleanBefore = before.replace(/\b(fas|far|fab)\s*/g, '')
      return `lucide-icon="${lucideIcon}" class="${cleanBefore}${after}"`
    }
    return match
  })
}
